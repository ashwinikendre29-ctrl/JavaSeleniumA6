package Loops;

public class looping8 {

	public static void main(String[] args) 
	{
		int n=4;
		int i,j;
           for (i = 1; i <=n; i++)
           {
        	 
               for( j=n;j>=i;j--)
              {    
            	 
                  System.out.print(j+ " ");
                
                //  System.out.print(" ");
              }   
            
          
            
            System.out.println();
        }
    }
	}


